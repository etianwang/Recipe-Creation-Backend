/** 去重后的扩展食材（按名称保留首次出现） */
export function dedupeIngredients(
  items: CatalogIngredient[],
): CatalogIngredient[] {
  const map = new Map<string, CatalogIngredient>();
  for (const item of items) {
    if (!map.has(item.name)) map.set(item.name, item);
  }
  return [...map.values()];
}

