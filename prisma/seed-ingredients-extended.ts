function ing(
  name: string,
  category: IngredientCategory,
  taste?: string,
): CatalogIngredient {
  return taste ? { name, category, taste } : { name, category };
}

function list(
  names: string[],
  category: IngredientCategory,
  taste?: string,
): CatalogIngredient[] {
  return names.map((name) => ing(name, category, taste));
}

